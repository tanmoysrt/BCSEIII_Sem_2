public class Student {
    final int roll;
    String deptCode;
    String name;
    String address;
    String phone;

    public Student(int roll, String deptCode, String name, String address, String phone) {
        this.roll = roll;
        this.deptCode = deptCode;
        this.name = name;
        this.address = address;
        this.phone = phone;
    }
}
